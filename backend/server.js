// normalizePort renvoi un port valide, qu'il soit fourni sous la forme d'un numéro ou d'une chaine
const normalizePort = val => {
    const port = parseInt(val, 10);
  
    if (isNaN(port)) {
      return val;
    }
    if (port >= 0) {
      return port;
    }
    return false;
  };
